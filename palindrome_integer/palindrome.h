#ifndef PALINDROME_H
#define PALINDROME_H

/**
 * is_palindrome - Vérifie si un nombre entier non signé est un palindrome
 * @n: Le nombre à vérifier
 *
 * Description: Un palindrome est un nombre qui se lit de la même façon
 * dans les deux sens (exemple: 121, 1001, 9)
 *
 * Return: 1 si n est un palindrome, 0 sinon
 */
int is_palindrome(unsigned long n);

#endif /* PALINDROME_H */
